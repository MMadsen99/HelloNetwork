public class TestSubStrings {


    public static void main(String[] args) {
        String name = "MathiasMadsen";
        System.out.println("Original String: " + name);
        System.out.println("Substring starting from index 7: " + name.substring(7));
        System.out.println("Substring starting from index 0 to 7: " + name.substring(0,7));
    }

}
